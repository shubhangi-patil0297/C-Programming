
#include <stdio.h>

int main() {
    char charConstant = 'A';
    printf("Character Constant: %c\n", charConstant);

    // Integer Constants
    int integerConstant = 42;
    printf("Integer Constant: %d\n", integerConstant);

    // Floating-point Constants
    float floatConstant = 3.14159;
    double doubleConstant = 2.71828;
    printf("Float Constant: %.5f\n", floatConstant);
    printf("Double Constant: %.5lf\n", doubleConstant);

    return 0;
}