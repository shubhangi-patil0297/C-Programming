#include<stdio.h>


int main()
{
    // integer 
    int integerNumber = 100;
    printf("Integer Number: %d\n", integerNumber);
    
    // float datatype
    float floatNumber = 100.0;
    printf("Float Number: %f\n",floatNumber);
    
    // double datatype
    double doubleNumber = 100.0;
    printf("Double Number: %lf\n", doubleNumber);
    
    // character datatype
    char character = 'A';
    printf("Character: %c\n", character);
    
    return 0;
}